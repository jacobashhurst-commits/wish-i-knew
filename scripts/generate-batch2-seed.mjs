function sqlString(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlJson(value) {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

function sqlBool(value) {
  return value ? "true" : "false";
}

function timingFields(card) {
  if (card.card_type === "quiet_week") {
    return "null, null, null, null";
  }
  if (card.start_age_days != null) {
    return `${card.start_age_days}, ${card.end_age_days}, null, null`;
  }
  return `null, null, ${card.pregnancy_week_start}, ${card.pregnancy_week_end}`;
}

function conditionsField(card) {
  const c = card.conditions ?? {};
  if (card.card_type === "quiet_week") return "'{}'::jsonb";
  return sqlJson(c);
}

function publishFields(card) {
  const needsReview =
    String(card.source_notes ?? "")
      .toLowerCase()
      .includes("review before production use") ||
    Boolean(
      card.medical_sensitivity ||
        card.government_sensitivity ||
        card.safety_sensitivity ||
        card.allergy_sensitivity ||
        card.feeding_sensitivity,
    );

  if (needsReview) {
    return `'approved',\n  'in_review',\n  null,\n  null,\n  null`;
  }

  return `'approved',\n  'published',\n  current_date,\n  current_date + interval '12 months',\n  now()`;
}

